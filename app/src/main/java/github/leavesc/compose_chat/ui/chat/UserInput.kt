@Composable
fun UserInputSelector(
    modifier: Modifier = Modifier,
    currentInputSelector: InputSelector,
    onInputSelectorChange: (InputSelector) -> Unit,
    sendMessageEnabled: Boolean,
    onMessageSent: () -> Unit,
) {
    Row(
        modifier = modifier
            .wrapContentHeight()
            .height(height = 56.dp),
        verticalAlignment = Alignment.CenterVertically
    ) {
        InputSelectorButton(
            icon = Icons.Outlined.Mood,
            selected = currentInputSelector == InputSelector.EMOJI,
            onClick = {
                onInputSelectorChange(InputSelector.EMOJI)
            }
        )
        InputSelectorButton(
            icon = Icons.Outlined.Topic,
            selected = currentInputSelector == InputSelector.Picture,
            onClick = {
                onInputSelectorChange(InputSelector.Picture)
            }
        )
        Spacer(modifier = Modifier.weight(1f))
        val contentColor = MaterialTheme.colors.primary
        val disabledContentColor = contentColor.copy(alpha = ContentAlpha.disabled)
        val buttonColors = ButtonDefaults.buttonColors(
            backgroundColor = contentColor,
            contentColor = contentColor,
            disabledBackgroundColor = disabledContentColor,
            disabledContentColor = disabledContentColor
        )
        Button(
            modifier = Modifier
                .padding(horizontal = 8.dp)
                .height(height = 36.dp),
            enabled = sendMessageEnabled,
            onClick = onMessageSent,
            colors = buttonColors,
            shape = MaterialTheme.shapes.large,
            contentPadding = PaddingValues(0.dp)
        ) {
            Text(
                modifier = Modifier.padding(horizontal = 16.dp),
                text = "Send"
            )
        }
    }
}

@Composable
private fun InputSelectorButton(
    icon: ImageVector,
    selected: Boolean,
    onClick: () -> Unit,
) {
    IconButton(onClick = onClick) {
        Icon(
            modifier = Modifier
                .padding(all = 8.dp)
                .size(size = 26.dp),
            imageVector = icon,
            tint = if (selected) {
                MaterialTheme.colors.primary
            } else {
                MaterialTheme.colors.primary.copy(alpha = ContentAlpha.disabled)
            },
            contentDescription = null
        )
    }
}

@Composable
fun EmojiTable(
    modifier: Modifier = Modifier,
    onTextAdded: (String) -> Unit
) {
    Column(
        modifier = modifier
            .fillMaxWidth()
    ) {
        repeat(4) { x ->
            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceAround
            ) {
                repeat(EMOJI_COLUMNS) { y ->
                    val emoji = emojis[x * EMOJI_COLUMNS + y]
                    Text(
                        modifier = Modifier
                            .clickable(onClick = {
                                onTextAdded(emoji)
                            })
                            .weight(weight = 1f)
                            .padding(vertical = 14.dp),
                        text = emoji,
                        style = TextStyle(
                            fontFamily = FontFamily.Default,
                            fontWeight = FontWeight.Light,
                            fontSize = 19.sp,
                            color = Color.Black.copy(alpha = 1f),
                            textAlign = TextAlign.Center,
                        ),
                        textAlign = TextAlign.Center,
                    )
                }
            }
        }
    }
}

